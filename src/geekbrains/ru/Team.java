package geekbrains.ru;

public enum Team {
    Dog(10, 1), Catty(2, 2), Boris(20, 2), Will(100, 10), Mouse(1, 1);

    private int maxRunDistance;

    Team(int maxRunDistance, int maxHeight)
    {
        this.maxRunDistance = maxRunDistance;
    }

    public int getMaxRunDistance()
    {
        return getMaxRunDistance();
    }

    public int getMaxHeight()
    {
        return getMaxHeight();
    }
}
